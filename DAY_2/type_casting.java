public class type_casting {
    public static void main(String args[]){
        // Scanner sc = new Scanner(System.in);
        // Lossy Conversion
        float a = 25.12f;
        int b = (int) a;
        System.out.println(b);


        // ASCII Value
        // converting character to integer
        char ch = 'a';
        int n = ch;
        System.out.println(n);
    }
}
