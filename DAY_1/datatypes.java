public class datatypes {
    public static void main(String args[]){
        int a = 4;
        System.out.println(a);
        byte b = 8;
        System.out.println(b);
        char ch = 'a';
        System.out.println(ch);
        boolean var = false;
        System.out.println(var);
        float price = 10.4F;
        System.out.println(price);
        // long
        // double
        short n = 240;
        System.out.println(n);
    }
}
