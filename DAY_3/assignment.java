public class assignment {
    public static void main(String args[]){
        // =   +=   -=   *=   /=   %=
        int a = 10;
        // a = a + 10
        a += 10;  // same as line 5
        System.out.println(a);

        int b = 5;
        // b = b-5;
        b -= 5;  // same as line 10
        System.out.println(b);

        int c = 10;
        // c = c * 10;
        c *= 10;  // same as line 15
        System.out.println(c);

        int d = 10;
        // d = d / 10;
        d /= 10;  // same as line 15
        System.out.println(d);

        int e = 10;
        // e = e / 10;
        e %= 3;  // same as line 15
        System.out.println(e);

        
    }
}
